      <h3>Co je permanentní make-up ?</h3>
            <p>
              Permanentní make-up je metoda dočasné pigmentace pokožky, která na dobu 2 až 3 let umožní zapomenout na denní líčení. 
              PM je implantace barevných pigmentů pomocí speciálních jehel do nejsvrchnější vrstvy pokožky (epidermis). 
            </p>
            <p>
              Samotný výkon se provádí speciálním kosmetickým přístrojem,  čepelky pomocí jemně vibrující tetovací jehly. 
              Do kůže se tak aplikuje barvivo, jehož intenzitu si každý zájemce po konzultaci vybere sám.
            </p>
            <p>
              Barvivo se z kůže, která podléhá přirozené obnově, se šupinkami odumřelé pokožky, postupně vytratí. 
              Po čase má klient možnost volby – buď celý zákrok podstoupit znovu s přihlédnutím k novým trendům 
              a také s ohledem na přirozené stárnutí pokožky, měnící se proporce v obličeji, apod., nebo od něj 
              upustí bez jakýchkoliv následků.
            </p>
            <p>
              Permanentní make – up dokáže také vyřešit jisté nedokonalosti obličeje. Chybí-li vám část obočí, je-li příliš řídké nebo úzké, 
              již si jej nemusíte dokreslovat.
            </p>

          <h3>
            Permanentní make up není náhražkou klasického líčení. Technika kresby může být až vlasově plasticky jemná, kterou nikdy nedocílíte běžnými líčidly.
          </h3>
          <p>
            Správně aplikovaný permanentní make up je tak přirozený, že vaše okolí nikdy nepozná, že ho máte a přitom váš obličej vypadá svěží a upravený.
          </p>

          <h3>
